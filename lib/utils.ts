export const AuthformSchema = (type: string) =>
  z.object({
    name:
      type === "sign-up"
        ? z.string().min(2, { message: "Name is required" })
        : z.string().optional(),
    branch:
      type === "sign-up"
        ? z.string().min(1, { message: "branch name is required" })
        : z.string().optional(),
    semester:
      type === "sign-up"
        ? z.string().min(1, { message: "semester is required" })
        : z.string().optional(),
    email: z.string().email({ message: "Email is required" }),
  });
